-------------------------------------------------------------------------------
-- DCM Definitions: Grants
-- Assigns privileges to each role following least-privilege principle
-- Uses _DEV for environment isolation
-------------------------------------------------------------------------------

-- =============================================================================
-- TURBO_LOADER: Can load data into RAW schema
-- =============================================================================

GRANT USAGE ON DATABASE TURBO_DEVOPS_DB_DEV TO ROLE TURBO_LOADER_DEV;
GRANT USAGE ON SCHEMA TURBO_DEVOPS_DB_DEV.RAW TO ROLE TURBO_LOADER_DEV;
GRANT CREATE TABLE ON SCHEMA TURBO_DEVOPS_DB_DEV.RAW TO ROLE TURBO_LOADER_DEV;
GRANT USAGE ON SCHEMA TURBO_DEVOPS_DB_DEV.UTILITIES TO ROLE TURBO_LOADER_DEV;
GRANT USAGE ON WAREHOUSE TURBO_LOADING_WH_DEV TO ROLE TURBO_LOADER_DEV;

-- =============================================================================
-- TURBO_TRANSFORMER: Reads RAW, writes STAGING and MARTS
-- =============================================================================

GRANT USAGE ON DATABASE TURBO_DEVOPS_DB_DEV TO ROLE TURBO_TRANSFORMER_DEV;
GRANT USAGE ON SCHEMA TURBO_DEVOPS_DB_DEV.RAW TO ROLE TURBO_TRANSFORMER_DEV;
GRANT SELECT ON FUTURE TABLES IN SCHEMA TURBO_DEVOPS_DB_DEV.RAW TO ROLE TURBO_TRANSFORMER_DEV;
GRANT USAGE ON SCHEMA TURBO_DEVOPS_DB_DEV.STAGING TO ROLE TURBO_TRANSFORMER_DEV;
GRANT CREATE TABLE ON SCHEMA TURBO_DEVOPS_DB_DEV.STAGING TO ROLE TURBO_TRANSFORMER_DEV;
GRANT CREATE VIEW ON SCHEMA TURBO_DEVOPS_DB_DEV.STAGING TO ROLE TURBO_TRANSFORMER_DEV;
GRANT USAGE ON SCHEMA TURBO_DEVOPS_DB_DEV.MARTS TO ROLE TURBO_TRANSFORMER_DEV;
GRANT CREATE TABLE ON SCHEMA TURBO_DEVOPS_DB_DEV.MARTS TO ROLE TURBO_TRANSFORMER_DEV;
GRANT CREATE VIEW ON SCHEMA TURBO_DEVOPS_DB_DEV.MARTS TO ROLE TURBO_TRANSFORMER_DEV;
GRANT USAGE ON WAREHOUSE TURBO_TRANSFORM_WH_DEV TO ROLE TURBO_TRANSFORMER_DEV;

-- =============================================================================
-- TURBO_ANALYST: Read-only on MARTS
-- =============================================================================

GRANT USAGE ON DATABASE TURBO_DEVOPS_DB_DEV TO ROLE TURBO_ANALYST_DEV;
GRANT USAGE ON SCHEMA TURBO_DEVOPS_DB_DEV.MARTS TO ROLE TURBO_ANALYST_DEV;
GRANT SELECT ON FUTURE TABLES IN SCHEMA TURBO_DEVOPS_DB_DEV.MARTS TO ROLE TURBO_ANALYST_DEV;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA TURBO_DEVOPS_DB_DEV.MARTS TO ROLE TURBO_ANALYST_DEV;
GRANT USAGE ON WAREHOUSE TURBO_ANALYTICS_WH_DEV TO ROLE TURBO_ANALYST_DEV;